namespace سست.PluginPriority
{
    /// <summary>
    /// Represents the priority of a plugin.
    /// </summary>
    public enum PluginPriority
    {
        /// <summary>
        /// Highest priority.
        /// </summary>
        Highest = 0,

        /// <summary>
        /// High priority.
        /// </summary>
        High = 1,

        /// <summary>
        /// Medium priority.
        /// </summary>
        Medium = 2,

        /// <summary>
        /// Low priority.
        /// </summary>
        Low = 3,

        /// <summary>
        /// Lowest priority.
        /// </summary>
        Lowest = 4,

        /// <summary>
        /// Default priority.
        /// </summary>
        Default = 5,

        /// <summary>
        /// Last priority.
        /// </summary>
        Last = 6
    }
}